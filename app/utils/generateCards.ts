// Keep existing interfaces and constants for compatibility
interface ColumnRange {
  letter: string;
  min: number;
  max: number;
}

export const columnRanges: ColumnRange[] = [
  { letter: 'B', min: 1, max: 15 },
  { letter: 'I', min: 16, max: 30 },
  { letter: 'N', min: 31, max: 45 },
  { letter: 'G', min: 46, max: 60 },
  { letter: 'O', min: 61, max: 75 }
];

// Cache
let cachedCards: number[][][] | null = null;

export const generateBingoCards = (count: number = 100): number[][][] => {
  if (cachedCards && cachedCards.length === count) {
    return cachedCards;
  }

  if (count > MANUAL_CARDS.length) {
    throw new Error(`Only ${MANUAL_CARDS.length} manual cards available.`);
  }

  cachedCards = MANUAL_CARDS.slice(0, count);
  return cachedCards;
};

export const getCardById = (id: number): number[][] => {
  if (id < 1 || id > MANUAL_CARDS.length) {
    throw new Error(`Card ID must be between 1 and ${MANUAL_CARDS.length}`);
  }
  return MANUAL_CARDS[id - 1];
};

export const validateCard = (card: number[][]): boolean => {
  if (!card || card.length !== 5) return false;

  for (let col = 0; col < 5; col++) {
    const column = card[col];
    if (!column || column.length !== 5) return false;

    for (let row = 0; row < 5; row++) {
      const num = column[row];
      if (col === 2 && row === 2) {
        if (num !== 0) return false;
        continue;
      }
      const range = columnRanges[col];
      if (!range || typeof num !== 'number' || num < range.min || num > range.max) {
        return false;
      }
    }
  }
  return true;
};

export const getTotalCards = (): number => MANUAL_CARDS.length;

export const resetCache = (): void => {
  cachedCards = null;
};

export const getAllCards = (): number[][][] => {
  if (!cachedCards) {
    cachedCards = generateBingoCards();
  }
  return cachedCards;
};